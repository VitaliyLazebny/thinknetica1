# 4. Заполнить хеш гласными буквами,
# где значением будет являтся порядковый
# номер буквы в алфавите (a - 1).

abc    = ('a'..'z').to_a
vowels = ['a', 'e', 'i', 'o', 'u', 'y']
hash   = {}

vowels.each do |v|
  hash[v] = abc.index(v)
end

puts hash
# {"a"=>0, "e"=>4, "i"=>8, "o"=>14, "u"=>20, "y"=>24}

